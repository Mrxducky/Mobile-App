export function haversineDistance(
  lat1: number, lon1: number,
  lat2: number, lon2: number
): number {
  const R = 6371000;
  const φ1 = (lat1 * Math.PI) / 180;
  const φ2 = (lat2 * Math.PI) / 180;
  const Δφ = ((lat2 - lat1) * Math.PI) / 180;
  const Δλ = ((lon2 - lon1) * Math.PI) / 180;
  const a =
    Math.sin(Δφ / 2) * Math.sin(Δφ / 2) +
    Math.cos(φ1) * Math.cos(φ2) * Math.sin(Δλ / 2) * Math.sin(Δλ / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function getNearestMachines(
  userLat: number,
  userLon: number,
  count = 10
): (CodMachine & { distance: number; eta: string })[] {
  return COD_MACHINES
    .map((m) => {
      const dist = haversineDistance(userLat, userLon, m.latitude, m.longitude);
      const speedMs = 8.33;
      const seconds = dist / speedMs;
      const minutes = Math.round(seconds / 60);
      const eta = minutes < 60 ? `${minutes} min` : `${Math.round(minutes / 60)}h ${minutes % 60}m`;
      return { ...m, distance: dist, eta };
    })
    .sort((a, b) => a.distance - b.distance)
    .slice(0, count);
}

export function formatDistance(meters: number): string {
  if (meters < 1000) return `${Math.round(meters)} m`;
  return `${(meters / 1000).toFixed(1)} km`;
}
